class ContactEntity {
  final int userId;
  final String userName;
  final String phone;
  final String email;

  final String phoneContact;
  final String color;
  final int idContact;
  final String nameContact;

  ContactEntity({
    required this.userId,
    required this.userName,
    required this.phone,
    required this.email,
    required this.phoneContact,
    required this.color,
    required this.idContact,
    required this.nameContact,
  });
}
